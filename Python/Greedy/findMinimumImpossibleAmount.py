# [문제 요약]
# - N개의 동전이 주어졌을 때, 이 동전들로 만들 수 없는 양의 정수 금액 중 최솟값 구하기
#
# [문제 해결 방법]
# 1. 동전을 오름차순으로 정렬
# 2. 현재까지 만들 수 있는 금액의 최댓값(target)을 1로 초기화
# 3. 각 동전을 순회하면서:
#    - 만약 현재 동전이 target보다 크다면, target이 만들 수 없는 최소 금액
#    - 그렇지 않다면, target에 현재 동전의 값을 더함
#
# [동전 아이디어]
# 동전은 더하기(+)만 가능합니다 (1원 + 2원 = 3원)
# 뺄셈(-)은 허용되지 않습니다 (5원 - 1원 = 4원은 불가능)
# 각 동전은 사용하거나 사용하지 않거나 둘 중 하나입니다 (부분적으로 사용 불가)
#
# [핵심 원리]
# "지금까지 만들 수 있는 금액의 범위(target)보다 큰 동전이 나오면, 그 target 금액은 만들 수 없다."
# 지금까지 만들 수 있는 범위보다 더 큰 동전이 나오는 순간, 그 target이 우리가 만들 수 없는 최소 금액이다.

# 동전개수 입력
n = int(input())
coins = list(map(int, input().split()))
coins.sort()

target = 1

for coin in coins:
    # 만들 수 없는 금액을 찾았을 때 반복 종료
    if coin > target:
        break

    target += coin

print(target)
